#include <unistd.h>

int		main(int argc, char **argv)
{
	int i = 0;
	while (argv[0][i])
	{
		i++;
	}
	if (*argv[0] && argc)
	{
		write(1, argv[0], i);
	}
	write (1, "\n", 1);
	return 0;
}
